            int collectionId,
            SingleQueryCollectionContext singleQueryCollectionContext)
        {
            while (Collections.Count <= collectionId)
            {
                Collections.Add(null);
            }

            Collections[collectionId] = singleQueryCollectionContext;
        }
    }
